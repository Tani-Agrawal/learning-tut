/* Equal to Comparison Operators */
var a = 10;
var b = 20;
console.log(a == b);
console.log("\n");

/* Equal value and equal type Comparison Operators */
var c = 10;
var d = "10";
console.log(c === d);
console.log("\n");

/* Not Equal Comparison Operators */
var e = 2;
var f = 3;
console.log(e != f);
console.log("\n");

/* Not Equal value or not equal type Comparison Operators */
var g = 2;
var h = '2';
console.log(g !== h);
console.log("\n");

/* Greater Than Comparison Operators */
var i = 2;
var j = 4;
console.log(i > j);
console.log("\n");

/* Less Than Comparison Operators */
var m = 10;
var n = 20;
console.log(m < n);
console.log("\n");

/* Greater Than or Equal To Comparison Operators */
var p = 10;
var q = 20;
console.log(p >= q);
console.log("\n");

/* Less Than or Equal To Comparison Operators */
var x = 20;
var y = 20;
console.log(x <= y);
console.log("\n");
