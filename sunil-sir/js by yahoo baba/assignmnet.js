/* Addition Assignment Operators */
var a = 10;
var b = 3;
a += b;
console.log(a);
console.log("\n");

/* Subtraction Assignment Operators */
var m = 10;
var n = 3;
m -= n;
console.log(m);
console.log("\n");

/* Multiplication Assignment Operators */
var p = 10;
var q = 3;
p *= q;
console.log(p);
console.log("\n");

/* Exponentiation Assignment Operators */
var d = 10;
var e = 3;
d **= e;
console.log(d);
console.log("\n");

/* Division Assignment Operators */
var s = 10;
var t = 3;
s /= t;
console.log(s);
console.log("\n");

/* Modulus (Remainder) Assignment Operators */
